package com.isoftstone.model.ydms;

public class TaskSZ {
	private String id;
	private String task_name;
	private String research_content;
	private String check_target;
	private String assume_org;
	private String join_org;
	private String outlay;
	private String sort_no;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTask_name() {
		return task_name;
	}
	public void setTask_name(String task_name) {
		this.task_name = task_name;
	}
	public String getResearch_content() {
		return research_content;
	}
	public void setResearch_content(String research_content) {
		this.research_content = research_content;
	}
	public String getCheck_target() {
		return check_target;
	}
	public void setCheck_target(String check_target) {
		this.check_target = check_target;
	}
	public String getAssume_org() {
		return assume_org;
	}
	public void setAssume_org(String assume_org) {
		this.assume_org = assume_org;
	}
	public String getJoin_org() {
		return join_org;
	}
	public void setJoin_org(String join_org) {
		this.join_org = join_org;
	}
	public String getOutlay() {
		return outlay;
	}
	public void setOutlay(String outlay) {
		this.outlay = outlay;
	}
	public String getSort_no() {
		return sort_no;
	}
	public void setSort_no(String sort_no) {
		this.sort_no = sort_no;
	}
	
	

}
